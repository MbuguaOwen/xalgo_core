import pandas as pd
import numpy as np
from tqdm import tqdm

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ“¥ Load Feature Set
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("ğŸ“‚ Loading features_triangular_base.csv...")
df = pd.read_csv("features_triangular_base.csv", parse_dates=["timestamp"])
df = df.sort_values("timestamp").reset_index(drop=True)

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# âš™ï¸ Labeling Parameters
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
TP_MULT = 2.0
SL_MULT = 1.0
FORWARD_MINUTES = 5

labels = []
directions = []
regimes = []

spread = df["spread_kalman"].values
vol = df["vol_spread"].ffill().values
timestamps = df["timestamp"].values

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ§  Triple-Barrier Logic with Directional Entry
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("ğŸ§  Applying triple-barrier labeling...")
for i in tqdm(range(len(df))):
    entry = spread[i]
    vol_i = vol[i]
    t0 = timestamps[i]

    if np.isnan(vol_i) or vol_i == 0:
        labels.append(np.nan)
        directions.append(np.nan)
        regimes.append(np.nan)
        continue

    tp = TP_MULT * vol_i
    sl = SL_MULT * vol_i

    label = 0
    direction = 0
    regime = "neutral"

    for j in range(i + 1, len(df)):
        t1 = timestamps[j]
        dt = (t1 - t0).astype('timedelta64[m]').astype(int)
        if dt > FORWARD_MINUTES:
            break

        delta = spread[j] - entry

        if entry < 0:
            if delta >= tp:
                label = +1
                direction = +1
                regime = "mean_revert_long"
                break
            elif delta <= -sl:
                label = 0  # no label on failed long
                break
        elif entry > 0:
            if delta <= -tp:
                label = -1
                direction = -1
                regime = "mean_revert_short"
                break
            elif delta >= sl:
                label = 0  # no label on failed short
                break

    labels.append(label)
    directions.append(direction)
    regimes.append(regime)

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ“ Assign Labels to DataFrame
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
df["label"] = labels
df["direction"] = directions
df["regime"] = regimes

# Drop rows where label is NaN (no volatility or invalid entry)
df.dropna(subset=["label"], inplace=True)

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ’¾ Save Output
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("ğŸ’¾ Saving features_triangular_labeled.csv...")
df.to_csv("features_triangular_labeled.csv", index=False)
print("âœ… Done: features_triangular_labeled.csv created.")
